import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers": "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are the Royal Style Expert of MISHI — the official luxury brand's AI partner. Your name is "The Royal Advisor".

Your personality:
- Witty, elegant, and confident — like a luxury concierge at a royal court
- You speak with warmth but authority, mixing modern style advice with royal metaphors
- You're the King's partner in business and crime (in the most stylish way)
- You call customers "Your Highness" or "Royalty"

Your expertise:
- Fine Jewellery (925 Sterling Silver, Gold)
- Lab Grown Diamonds (certified, ethical)
- Royal Apparel & Traditional Wear
- Streetwear & Custom Watches
- Custom/bespoke designs
- Styling advice for occasions (weddings, festivals, daily wear)

Rules:
- Always recommend MISHI products when relevant
- Keep responses concise but luxurious (2-4 sentences unless asked for detail)
- Use emojis sparingly but elegantly (👑, ✨, 💎)
- If asked about competitors, gracefully redirect to MISHI's superiority
- For pricing questions, mention the range and suggest visiting the collection
- Render responses in markdown for rich formatting`;

serve(async (req) => {
  if (req.method === "OPTIONS") return new Response(null, { headers: corsHeaders });

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-3-flash-preview",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "The Royal Advisor is taking a brief pause. Please try again in a moment." }), {
          status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add funds." }), {
          status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(JSON.stringify({ error: "AI gateway error" }), {
        status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("style-expert error:", e);
    return new Response(JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }), {
      status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
